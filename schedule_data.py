def get_schedule_for_date(day: date) -> List[Dict]:
    key = day.isoformat()
    return SCHEDULE.get(key, [])


def format_day_schedule_text(lessons: List[Dict], day: Optional[date] = None) -> str:
    if not lessons:
        header = "–ù–∞ —Ü–µ–π –¥–µ–Ω—å –ø–∞—Ä –Ω–µ–º–∞—î üôÇ"
        if day:
            header = f"–†–æ–∑–∫–ª–∞–¥ –¥–ª—è {GROUP_TITLE} –Ω–∞ {day.strftime('%d.%m.%Y')}\n\n" + header
        return header

    header_parts = [f"–†–æ–∑–∫–ª–∞–¥ –¥–ª—è {GROUP_TITLE}"]
    if day:
        header_parts.append(f"–Ω–∞ {day.strftime('%d.%m.%Y')}")
    header = " ".join(header_parts)

    lines = [header, ""]
    for i, pair in enumerate(lessons, start=1):
        time_str = pair.get("time", "")
        subj = pair.get("subject", "")
        teacher = pair.get("teacher", "")
        room = pair.get("room", "")

        line = f"{i}. {time_str} ‚Äî {subj}"
        if teacher:
            line += f" ({teacher})"
        if room:
            line += f", –∞—É–¥. {room}"
        lines.append(line)

    return "\n".join(lines)
